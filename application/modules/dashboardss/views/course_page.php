<script type="text/javascript">
  function handleCartItems(elem) {
    url1 = '<?php echo site_url('guest/handle/CartItems'); ?>';
    url2 = '<?php echo site_url('guest/refreshWishList'); ?>';
    $.ajax({
      url: url1,
      type: 'POST',
      data: {
        course_id: elem.id
      },
      success: function(response) {
        $('#cart_items').html(response);
        if ($(elem).hasClass('addedToCart')) {
          $(elem).removeClass('addedToCart')
          $(elem).text("Add to cart");
        } else {
          $(elem).addClass('addedToCart')
          $(elem).text("Added to cart");
        }
        $.ajax({
          url: url2,
          type: 'POST',
          success: function(response) {
            $('#wishlist_items').html(response);
          }
        });
      }
    });
  }

  function handleBuyNow(elem) {

    url1 = '<?php echo site_url('guest/handle/CartItemForBuyNowButton'); ?>';
    url2 = '<?php echo site_url('guest/refresh/WishList'); ?>';
    urlToRedirect = '<?php echo site_url('guest/shopping_cart'); ?>';
    var explodedArray = elem.id.split("_");
    var course_id = explodedArray[1];

    $.ajax({
      url: url1,
      type: 'POST',
      data: {
        course_id: course_id
      },
      success: function(response) {
        $('#cart_items').html(response);
        $.ajax({
          url: url2,
          type: 'POST',
          success: function(response) {
            $('#wishlist_items').html(response);
            toastr.warning('Please wait....');
            setTimeout(
              function() {
                window.location.replace(urlToRedirect);
              }, 1500);
          }
        });
      }
    });
  }

  function handleEnrolledButton() {
    $.ajax({
      url: '<?php echo site_url('guest/isLoggedIn'); ?>',
      success: function(response) {
        if (!response) {
          window.location.replace("<?php echo site_url('guest/login'); ?>");
        }
      }
    });
  }

  function handleAddToWishlist(elem) {
    $.ajax({
      url: '<?php echo site_url('guest/handle/WishList'); ?>',
      type: 'POST',
      data: {
        course_id: elem.id
      },
      success: function(response) {
        if (!response) {
          window.location.replace("<?php echo site_url('guest/login'); ?>");
        } else {
          if ($(elem).hasClass('active')) {
            $(elem).removeClass('active');
            $(elem).text("Add to wishlist");
          } else {
            $(elem).addClass('active');
            $(elem).text("Added to wishlist");
          }
          $('#wishlist_items').html(response);
        }
      }
    });
  }

  function pausePreview() {
    player.pause();
  }
</script>