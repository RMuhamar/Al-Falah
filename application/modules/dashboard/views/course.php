    function handleEnrolledButton() {
        $.ajax({
            url: 'http://localhost/academy/home/isLoggedIn',
            success: function(response) {
                if (!response) {
                    window.location.replace("http://localhost/academy/login");
                }
            }
        });
    }

    function handleAddToWishlist(elem) {
        $.ajax({
            url: 'http://localhost/academy/home/handleWishList',
            type: 'POST',
            data: {
                course_id: elem.id
            },
            success: function(response) {
                if (!response) {
                    window.location.replace("http://localhost/academy/login");
                } else {
                    if ($(elem).hasClass('active')) {
                        $(elem).removeClass('active');
                        $(elem).text("Add to wishlist");
                    } else {
                        $(elem).addClass('active');
                        $(elem).text("Added to wishlist");
                    }
                    $('#wishlist_items').html(response);
                }
            }
        });
    }

